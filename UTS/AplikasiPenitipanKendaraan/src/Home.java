    private void selectData() {
        String kolom[] = {"nama_petugas","no_resi","nopol","jenis_kendaraan","tgl_masuk","tgl_keluar","jml_harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_data";
        ResultSet rs = DBKoneksi.executeQuery(SQL);
        try{
            while(rs.next()){
                String nama_petugas = rs.getString(1);
                String no_resi = rs.getString(2);
                String nopol = rs.getString(3);
                String jenis_kendaraan = rs.getString(4);
                String tgl_masuk = rs.getString(5);
                String tgl_keluar = rs.getString(6);
                String jml_harga = rs.getString(7);
            }
        }catch (SQLException ex ){
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
    }

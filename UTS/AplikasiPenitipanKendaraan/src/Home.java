    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tglMasuk = dateFormat.format(jdMasuk.getDate());
        String tglKeluar = dateFormat.format(jdKeluar.getDate());
        int harga = 3000;
        hariawal = Integer.parseInt(tglMasuk);
        hariakhir = Integer.parseInt(tglKeluar);

        totalhari = hariakhir - hariawal;
        hargatotal = totalhari * harga;
        String hargafinal = Integer.toString(hargatotal);

        if ("".equals(tfPetugas.getText()) || "".equals(tfNoResi.getText()) || "".equals(tfNopol.getText()) ||
                "".equals(tfJenis.getText()) || "".equals(jdMasuk.getDate()) || "".equals(jdKeluar.getDate())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String SQL = "INSERT INTO `tb_data`(`nama_petugas`, `no_resi`, `nopol`, `jenis_kendaraan`, `tgl_msk`, `tgl_keluar`, `jml_harga`) "
                    + "VALUES('" + tfPetugas.getText() + "','" + tfNopol.getText() + "','"+tfJenis.getText()+"',"
                    + "'" + tglMasuk + "','" + tglKeluar + "','" + hargafinal + "')";
            int status = DBKoneksi.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

